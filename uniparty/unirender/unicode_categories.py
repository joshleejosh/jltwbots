def unicode_block_range(u):
    for b in BLOCKS:
        if b[0] <= u and b[1] >= u:
            return b
    return (0,0,'')

def unicode_block_name(u):
    for bmin, bmax, blab in BLOCKS:
        if bmin <= u and bmax >= u:
            return blab
    return ''

